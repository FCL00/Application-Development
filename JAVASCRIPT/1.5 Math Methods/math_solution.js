// 1. Generate a random integer betweem 1 to 10
let randomInt = Math.floor(Math.random() * 10) + 1;
console.log("1. Random integer", randomInt);

// 2. Calculate the square root of 64.
let squareRoot = Math.sqrt(64);
console.log("2. Square root of 64:", squareRoot);

// 3. Calculate the value of 3 raised to the power of 4.
let powerResult = Math.pow(3, 4);
console.log("3. 3 raised to the power of 4:", powerResult);

// 4. Calculate the area of a circle with radius 5. (Hint: Use Math.PI)
let radius = 5;
let areaOfCircle = Math.PI * Math.pow(radius, 2);
console.log("4. Area of a circle with radius 5:", areaOfCircle.toFixed(2));

// 5. Find the smallest integer greater than or equal to 6.7.
let ceilValue = Math.ceil(6.7);
console.log("5. Smallest integer greater than or equal to 6.7:", ceilValue);

// 6. Find the largest integer less than or equal to 6.7.
let floorValue = Math.floor(6.7);
console.log("6. Largest integer less than or equal to 6.7:", floorValue);

// 7. Round 4.2 to the nearest integer.
let roundedValue = Math.round(4.2);
console.log("7. Rounded value of 4.2:", roundedValue);

// 8. Find the maximum of 10, 20, and 30.
let maxNumber = Math.max(10, 20, 30);
console.log("8. Maximum value:", maxNumber);

// 9. Find the minimum of 10, 20, and 30.
let minNumber = Math.min(10, 20, 30);
console.log("9. Minimum value:", minNumber);
